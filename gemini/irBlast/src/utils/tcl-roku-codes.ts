export function createTclRokuRemote(): IrRemoteDefinition {
  const keyDefinitions: IrKeyDefinition[] = Object.entries(TCL_ROKU_TV_CODES).map(
    ([key, codes]) => ({
      logicalKeyRoleCode: key as any,
      displayLabelTextValue: key.replace(/([A-Z])/g, ' $1').trim(),
      irSignalEncodingFormatCode: 'prontoHex',
      irSignalEncodedDataTextValue: codes.prontoHex
    })
  );

  return {
    remoteUniqueKeyTextValue: 'tcl::roku-tv::default',
    manufacturerTextValue: 'TCL',
    modelTextValue: 'Roku TV',
    preferredKeySetKeyTextValue: 'fullTvKeySet.v1',
    keyDefinitions
  };
}
